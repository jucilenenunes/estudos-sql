Chaves primárias 
Tem a finalidade de identificar a linha inteira de uma coluna.
Identificando como índice, por isso é comum usar ID.

Chaves estrangeiras
Ela é apenas uma referência para buscar informações adicionais que estão em outras tabelas
Tabela referenciada pai e filho. Lembrando que uma tabela pode ter mais de 1 chave estrangeira. 
Ao contrario da chave primária que contém apenas 1.